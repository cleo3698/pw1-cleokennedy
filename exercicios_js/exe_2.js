
function verificarNumero(numero){
  
  if (numero % 2 === 0){
    console.log("O numero é PAR.");
  } else {
    console.log("O numero é IMPAR.");
  }

  if(numero> 0){
    console.log("o numero é POSITIVO.");
  } else if(numero <0){
    console.log("O numero é Negativo");
  } else {
    console.log("O numero é ZERO");
  }
}

